local box = {}

for i=1,#pix do
	for j=1,#pix[i] do
		if pix[i][j] == 1 then
			local y = (i-16)*-1+1
			local x = j-16
			table.insert(box, {(x-1)/32, (y-1)/32, -0.5/32, x/32, y/32, 0.5/32})
		end
	end
end

minetest.register_node("3d_player:box", {
	drawtype = "nodebox",
	tiles = {"3d_player_side.png", "3d_player_side.png", "3d_player_side.png", "3d_player_side.png", "3d_player.png", "3d_player_back.png"},
	node_box = {
		type = "fixed",
		fixed = box
	},
})

minetest.register_on_joinplayer(function(player)
	player:set_properties({
		visual = "wielditem",
		textures = {"3d_player:box"},
		visual_size = {x=0.667, y=0.667*2},
	})
end)

